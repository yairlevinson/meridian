/** Sorted function entries for dropdown */
export const FUNCTION_OPTIONS = Object.entries(SERVO_FUNCTIONS)
  .map(([value, label]) => ({ value: Number(value), label: `${label} (${value})` }))
  .sort((a, b) => a.value - b.value)

/** Get the function name for a servo channel, or null if not assigned / disabled */
export function getServoFunctionName(fnValue: number | undefined): string | null {
  if (fnValue === undefined || fnValue === 0) return null
  return SERVO_FUNCTIONS[fnValue] ?? null
}
